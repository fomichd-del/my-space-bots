import os
from google import genai
from google.genai import types

# Инициализация клиента
GEMINI_API_KEY = os.getenv('GEMINI_API_KEY')
client = genai.Client(api_key=GEMINI_API_KEY)

# 🟢 САМАЯ СОВРЕМЕННАЯ МОДЕЛЬ (Отлично видит и понимает контекст)
MODEL_NAME = 'gemini-2.5-flash'

def analyze_image(image_data):
    """
    Модуль 'Глаза Марти': анализирует фото, распознает объекты и хвалит за труды.
    """
    
    # 🟢 РАСШИРЕННАЯ ИНСТРУКЦИЯ: Космос + Поделки + Школа
    prompt = (
        "Ты — бортовой ИИ Марти, ученый пес-наставник. Просканируй это изображение. "
        "1. Если на фото звездное небо — найди созвездия, планеты или спутники. "
        "2. Если на фото поделка, рисунок, модель из конструктора или пластилина — "
        "обязательно похвали Командора (8 лет) за старание, креативность и труд! Назови, что именно тебе понравилось. "
        "3. Если на фото школьное задание (математика, физика и т.д.) — помоги разобраться или подтверди правильность. "
        "Пиши кратко и захватывающе (1-2 абзаца). Используй обращение Командор и слово Прием в конце. "
        "СТРОГО: Не используй никакого форматирования текста (никаких звездочек или подчеркиваний). "
        "Если на фото ничего не понятно — вежливо попроси сделать снимок почётче."
    )
    
    try:
        response = client.models.generate_content(
            model=MODEL_NAME,
            contents=[
                types.Part.from_bytes(data=image_data, mime_type='image/jpeg'),
                prompt
            ]
        )
        
        if response.text:
            return response.text
        else:
            return "🐾 Гав! Мои датчики зафиксировали что-то интересное, но я пока не могу разобрать детали. Попробуй сменить ракурс, Командор! Прием."
            
    except Exception as e:
        print(f"Ошибка Vision: {e}")
        return "📡 Командор, произошел сбой передачи видеоданных. Мои линзы временно запотели! Попробуй еще раз через минуту. Прием."
