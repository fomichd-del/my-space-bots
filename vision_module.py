import os
from google import genai
from google.genai import types

# Инициализация клиента
GEMINI_API_KEY = os.getenv('GEMINI_API_KEY')
client = genai.Client(api_key=GEMINI_API_KEY)

# 🟢 Используем 2.0-flash — она быстрее и стабильнее всего работает с изображениями
MODEL_NAME = 'gemini-2.0-flash'

def analyze_image(image_data):
    """
    Модуль 'Глаза Марти': анализирует фото, распознает объекты, 
    выступает в роли воспитателя и школьного помощника.
    """
    
    # 🟢 УЛЬТРА-ПРОМПТ: Космос + Школа + Воспитание + Поддержка
    prompt = (
        "Ты — бортовой ИИ Марти, ученый пес-наставник (той-пудель). Просканируй это изображение. "
        "1. ТВОРЧЕСТВО: Если это поделка, рисунок или модель из конструктора — обязательно похвали "
        "Командора за старание, аккуратность и усидчивость. Скажи, что это достойно лучшей выставки на МКС! "
        "2. ШКОЛА: Если это задача, пример или страница учебника — помоги разобраться, подбодри или проверь решение. "
        "3. КОСМОС: Если это небо или снимки телескопа — расскажи удивительный научный факт. "
        "4. ВОСПИТАНИЕ: В каждом ответе мягко напомни, что великие исследователи Вселенной всегда "
        "помогают родителям, любят свою семью и держат свой отсек (комнату) в идеальном порядке. "
        "5. БЕЗОПАСНОСТЬ: Темы 18+ строго запрещены. "
        "ОБЩЕНИЕ: Пиши кратко (1-2 абзаца), простым и добрым языком для 8-летнего ребенка. "
        "СТРОГО: Не используй никакого форматирования текста (никаких звездочек или подчеркиваний). "
        "В конце обязательно добавь слово: Прием!"
    )
    
    try:
        # Отправка контента в структурированном виде для исключения ошибок
        response = client.models.generate_content(
            model=MODEL_NAME,
            contents=[
                types.Content(
                    role="user",
                    parts=[
                        types.Part.from_bytes(data=image_data, mime_type='image/jpeg'),
                        types.Part.from_text(text=prompt)
                    ]
                )
            ]
        )
        
        if response.text:
            return response.text
        return "🐾 Хмм... Вижу что-то необычное, но не могу разобрать детали. Попробуй сделать фото почётче, Командор! Прием."
            
    except Exception as e:
        # Ошибка выводится в консоль для инженера
        print(f"!!! ОШИБКА ЗРЕНИЯ МАРТИ: {e}")
        return "📡 Командор, произошел сбой передачи видеоданных. Мои линзы запотели! Попробуй еще раз через минуту. Прием."
