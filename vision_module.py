import google.generativeai as genai
import os

# Подключаем ключ из переменных окружения Render
genai.configure(api_key=os.getenv("GEMINI_API_KEY"))

# Используем самую быструю и эффективную модель — Gemini 3 Flash
model = genai.GenerativeModel('gemini-1.5-flash') # Технический идентификатор

def marty_vision(image_data):
    """Марти анализирует изображение и отвечает как эксперт-астроном"""
    prompt = (
        "Ты — пес Марти, эксперт по космосу и верный друг Владика. "
        "Тебе прислали фотографию. Если на ней что-то научное, космическое или техническое — "
        "объясни это просто и захватывающе. Если на фото собака — радостно поприветствуй! "
        "Если на фото что-то другое — попробуй найти в этом связь с космосом (например, "
        "круглое яблоко похоже на планету). Отвечай по-русски, весело и с эмодзи. 🐾🚀"
    )
    
    try:
        # Передаем картинку и промпт в модель
        response = model.generate_content([
            prompt,
            {"mime_type": "image/jpeg", "data": image_data}
        ])
        return response.text
    except Exception as e:
        return f"🛰️ Упс! Космические помехи. Не удалось распознать фото. Ошибка: {e}"
